<#
.SYNOPSIS
  Phase 30 — Accountant Acceptance Package: generates a comprehensive markdown report
  combining all probe results (reconciliation, anomaly, aging, tax, cashflow) into a
  single document for accountant review and sign-off.
#>
param(
  [string]$FiscalYear = '1402',
  [string]$OutputFile = ''
)

$ErrorActionPreference = 'Stop'
$dateStr = Get-Date -Format 'yyyy-MM-dd'
if (-not $OutputFile) {
  $OutputFile = "scripts\ops\accountant-acceptance-$dateStr.md"
}

# Read all probe reports
$probeDir = 'scripts\ops'
$reports = @{}

$reportFiles = @{
  'reconciliation' = "reconciliation-$dateStr.md"
  'anomaly'        = "anomaly-probe-$dateStr.md"
  'aging'          = "aging-probe-$dateStr.md"
  'tax_checks'     = "tax-checks-probe-$dateStr.md"
  'cashflow'       = "cashflow-depreciation-probe-$dateStr.md"
}

foreach ($key in $reportFiles.Keys) {
  $path = Join-Path $probeDir $reportFiles[$key]
  if (Test-Path $path) {
    $reports[$key] = Get-Content $path -Raw -Encoding UTF8
    Write-Host "  Loaded: $path" -ForegroundColor Green
  } else {
    Write-Host "  Missing: $path" -ForegroundColor Yellow
    $reports[$key] = "*Report not found. Run the corresponding probe script first.*"
  }
}

# Build the acceptance package
$package = @"
# بسته پذیرش حسابدار — Accountant Acceptance Package
## Phase 30: Reconciliation & Metric Verification

**تاریخ تولید**: $dateStr
**سال مالی**: $FiscalYear
**سرور**: 192.168.85.56:2211 → SQL 127.0.0.1:58033 (Sepidar01)

---

## خلاصه اجرایی / Executive Summary

این بسته شامل نتایج اعتبارسنجی و تطبیق متریک‌های مالی ACC Assist با داده‌های مستقل از پایگاه داده سپیدار است. حسابدار محترم باید موارد زیر را بررسی و تأیید نماید:

1. **تطبیق فروش** — مقایسه جمع فاکتورها با جمع ارقام دفتر کل
2. **تطبیق خرید/موجودی/بانک** — مقایسه منابع مستقل
3. **کشف ناهنجاری** — سندهای ترازنشده، فاکتورهای صفر، سندهای تکراری
4. **تحلیل سنی** — سطل‌های سنی دریافتنی و پرداختنی
5. **مالیات و چک‌ها** — نرخ VAT، چک‌های سررسید و برگشتی
6. **جریان وجوه نقد و دارایی‌های ثابت** — روش غیرمستقیم و مستقیم

---

